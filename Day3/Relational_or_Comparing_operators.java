package Day3;

public class Relational_or_Comparing_operators {
	public static void main(String [] args) {
		
		int a = 10;
		int b = 20;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>=b);
		System.out.println(a<=b);
	}
}
